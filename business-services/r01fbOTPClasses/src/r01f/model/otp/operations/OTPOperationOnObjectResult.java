package r01f.model.otp.operations;

public interface OTPOperationOnObjectResult<T>
		 extends OTPOperationResult {
/////////////////////////////////////////////////////////////////////////////////////////
//  
/////////////////////////////////////////////////////////////////////////////////////////
	/**
	 * @return the model object type 
	 */
	public Class<T> getObjectType();
/////////////////////////////////////////////////////////////////////////////////////////
//  
/////////////////////////////////////////////////////////////////////////////////////////
	/**
	 * @return the requested operation
	 */
	public OTPRequestedOperation getRequestedOperation();
	/**
	 * @return the performed operation
	 */
	public OTPPerformedOperation getPerformedOperation();
}
