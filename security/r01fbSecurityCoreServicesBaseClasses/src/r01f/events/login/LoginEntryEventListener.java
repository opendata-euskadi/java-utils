package r01f.events.login;

import r01f.model.security.login.recovery.PasswordRecoveryResponseBase;
import r01f.model.security.login.reset.PasswordResetResponseBase;
import r01f.model.security.login.response.LoginResponseError;
import r01f.model.security.login.response.LoginResponseOK;

public class LoginEntryEventListener {

/////////////////////////////////////////////////////////////////////////////////////////
//Listens to LoginOperationEvent
/////////////////////////////////////////////////////////////////////////////////////////
	public static interface LoginEventListener<ROK extends LoginResponseOK,
											   RNOK extends LoginResponseError>
					extends LoginOKEventListener<ROK>,
							LoginErrorEventListener<RNOK> {

	}

/////////////////////////////////////////////////////////////////////////////////////////
//Listens to OK LoginOperationOKEvent
/////////////////////////////////////////////////////////////////////////////////////////
	public static interface LoginOKEventListener<ROK extends LoginResponseOK> {
		public void onLoginOK(final ROK opOKEvent);
	}
/////////////////////////////////////////////////////////////////////////////////////////
//Listens to NOK PersistenceOperationErrorEvent
/////////////////////////////////////////////////////////////////////////////////////////
	public static interface LoginErrorEventListener<RNOK extends LoginResponseError> {
		public void onLoginError(final RNOK opErrorEvent);
	}

/////////////////////////////////////////////////////////////////////////////////////////
//Listens to PasswordRecoveryEntryEvent
/////////////////////////////////////////////////////////////////////////////////////////
	public static interface PasswordRecoveryEventListener<R extends PasswordRecoveryResponseBase> {
		public void onPasswordRecovery(final R requestEvent);
	}

/////////////////////////////////////////////////////////////////////////////////////////
//Listens to PasswordResetEntryEvent
/////////////////////////////////////////////////////////////////////////////////////////
	public static interface PasswordResetEventListener<R extends PasswordResetResponseBase> {
		public void onPasswordReset(final R requestEvent);
	}

}
